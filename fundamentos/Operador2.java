public class Operador2{
  public static void main(String[] args){
    int total = 10;

    // Operador de soma com atribuição
    total += 3;
    System.out.println(total); // Imprime 13

    // Subtração com atribuição
    total -= 2;
    System.out.println(total); // Imprime 11

    // Multiplicação com atribuição
    total *= 3;
    System.out.println(total); // Imprime 33

    // Divisão com atribuição
    total /= 3;
    System.out.println(total); // Imprime 11

    // Módulo com atribuição
    total %= 3;
    System.out.println(total); // Imprime 2
  }
}
